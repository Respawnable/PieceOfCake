#include "rdpartyrobotcdr-3.3.1\drivers\hitechnic-irseeker-v2.h"
#include "JoystickDriver.c"

int _dirAC = 0;
int acS1, acS2, acS3, acS4, acS5 = 0;
int maxSig = 0; 					// the max signal strength from the seeker.
// These are the tuning variables. Set these based on results of test runs.
int wrist_pos = 26;				// At the rack height.
int irGoal = 7;						// 25, 26Where is the beacon.
int irStrengthGoal = 140; // 100<x<125 the IR strenght goal (we are on the rack).
int driveSpeed = 75;
int turnTime = 145; ///// Time (ms) to complete 90 degree turn.
int beaconDirection = 2; ///// Which side of the robot is the beacon on (1=left, 2= right)?
float movingAverageWeight = .3;

void initializeRobot()
{
	servoChangeRate[servoWrist] =20;          // Slow the Servo Change Rate down to only 4 positions per update.
	servo[servoWrist] = wrist_pos;
	wait1Msec(200);
}

void driveMotors(int lspeed, int rspeed)
{
	motor[motorL] = lspeed;
	motor[motorR] = rspeed;
}

int GetMovingAverage(float currentAvg, float newValue)
{
	return newValue * movingAverageWeight + (1 - movingAverageWeight) * currentAvg;
}

void MovetoIR()
{
	while(true)
	{
		_dirAC = HTIRS2readACDir(IRseeker);
		nxtDisplayCenteredBigTextLine(1, "IR: %d", _dirAC);

		switch(beaconDirection)
		{
		case 2:
			if (_dirAC < irGoal)
			{
				driveMotors(driveSpeed, driveSpeed);
			}

			if (_dirAC >= irGoal)
			{
				wait1Msec(2000);
				driveMotors(0, 0);
				wait1Msec(10);
				return;
				break;
			}
			break;
		case 1:
			if (_dirAC < irGoal)
			{
				driveMotors(driveSpeed, driveSpeed);
			}

			if (_dirAC >= irGoal)
			{
				driveMotors(0, 0);
				wait1Msec(10);
				break;
			}
			break;
		}
	}
}
// Turn 90 degrees to the beaconDirection .
void Turn90()
{
	int turnSpeed = 75;
	motor[motorL] = beaconDirection == 2 ? turnSpeed : -turnSpeed;
	motor[motorR] = -motor[motorL];
	wait10Msec(turnTime);
	motor[motorL] = 0;
	motor[motorR] = 0;
	nxtDisplayCenteredBigTextLine(2, "turnSpeed: %d",turnSpeed);
}

void MoveToRack()
{
	const int speedBase = 30;
	const int turnSpeed = 20;
	int dirVal = movingAverageWeight;
	int maxSigAvg = movingAverageWeight;

	while(true)
	{
		HTIRS2readAllACStrength(IRseeker, acS1, acS2, acS3, acS4, acS5);
		maxSig = (acS1 > acS2) ? acS1 : acS2;
		maxSig = (maxSig > acS3) ? maxSig : acS3;
		maxSig = (maxSig > acS4) ? maxSig : acS4;
		maxSig = (maxSig > acS5) ? maxSig : acS5;
		maxSig = GetMovingAverage(maxSigAvg, maxSig);
		nxtDisplayCenteredBigTextLine(3, "maxSig: %d", maxSig);
		if (maxSig < irStrengthGoal)
		{
			dirVal = GetMovingAverage(dirVal, (_dirAC - irGoal));
			driveMotors(speedBase + turnSpeed * dirVal, speedBase - turnSpeed * dirVal);
		}
		if (maxSig >= irStrengthGoal)
		{
			driveMotors(0, 0);
			wait1Msec(10);
			break;
		}
	}
}

task main()
{
	initializeRobot();
//	waitForStart();

	MovetoIR();
	Turn90();
	MoveToRack();
	servo[servoWrist] = 128;
	driveMotors(-25,-25);
	driveMotors(0,0);
	wait10Msec(1);

	PlaySound(soundFastUpwardTones);
	// Wait for teleop
	while (true)
	{
		PlaySound(soundFastUpwardTones);
	}
}
